from langchain_community.retrievers import BM25Retriever
from langchain_classic.retrievers import EnsembleRetriever
from langchain_classic.retrievers.document_compressors import CrossEncoderReranker
from langchain_community.cross_encoders import HuggingFaceCrossEncoder
from langchain_classic.retrievers import ContextualCompressionRetriever

print("\n" + "=" * 70)
print("🚀 Running Extension: Hybrid Retrieval + Re-Ranking")
print("=" * 70)

print("⏳ Building BM25 Retriever...")
bm25_retriever = BM25Retriever.from_documents(chunks)
bm25_retriever.k = 4

print("⏳ Combining into Hybrid Ensemble (60% Dense, 40% Sparse)...")
hybrid_retriever = EnsembleRetriever(
    retrievers=[retriever, bm25_retriever],
    weights=[0.6, 0.4],  # 60% dense, 40% BM25
)

print("⏳ Loading Cross-Encoder Re-Ranker...")
cross_encoder = HuggingFaceCrossEncoder(model_name="cross-encoder/ms-marco-MiniLM-L-6-v2")
reranker = CrossEncoderReranker(model=cross_encoder, top_n=3)

compression_retriever = ContextualCompressionRetriever(
    base_compressor=reranker,
    base_retriever=hybrid_retriever
)

ext_query = "What was Apple net income in fiscal 2023?"
print(f'\n🔍 Extension Query: "{ext_query}"')

t0 = time.time()
hybrid_results = compression_retriever.invoke(ext_query)
ext_latency = time.time() - t0

print(f"   ⏱️ Retrieved and Re-Ranked {len(hybrid_results)} chunks in {ext_latency:.2f}s")
for i, r in enumerate(hybrid_results):
    print(f"   [{i+1}] {r.metadata['source']}: {r.page_content[:100]}...")

print("\n✅ Pipeline complete!")
