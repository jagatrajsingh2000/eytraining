# Step 3 — Chunking Strategy
# ─────────────────────────────────────────────────────────────────────────────

from langchain_text_splitters import RecursiveCharacterTextSplitter
from langchain_core.documents import Document


def create_chunks(
    docs: list, chunk_size: int = 512, chunk_overlap: int = 64
) -> list:
    """Convert raw documents to LangChain Document chunks."""
    splitter = RecursiveCharacterTextSplitter(
        chunk_size=chunk_size,
        chunk_overlap=chunk_overlap,
        separators=["\n\n", "\n", ". ", " ", ""],
        length_function=len,
    )
    lc_docs = [
        Document(page_content=d["text"], metadata={"source": d["source"]})
        for d in docs
    ]
    chunks = splitter.split_documents(lc_docs)
    return chunks


CHUNK_SIZE = 512
CHUNK_OVERLAP = 64
chunks = create_chunks(SAMPLE_DOCS, CHUNK_SIZE, CHUNK_OVERLAP)

print(f"\n✅ Created {len(chunks)} chunks (size={CHUNK_SIZE}, overlap={CHUNK_OVERLAP})")
